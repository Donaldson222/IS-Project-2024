    (function ($) {
        "use strict";
    
        // ... (rest of the code remains the same)
    
        // Phone number validation function
        function validatePhoneNumber(input) {
            var validRegex = /^\d{3}-\d{3}-\d{4}$/;
            if (input.value.match(validRegex)) {
                return true;
            } else {
                alert("Invalid phone number! Please use the format XXX-XXX-XXXX.");
                return false;
            }
        }
    
        // Email validation function
        function validateEmail(input) {
            var validRegex = /^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)*$/;
            if (input.value.match(validRegex)) {
                return true;
            } else {
                alert("Invalid email address!");
                return false;
            }
        }
    
        // Call the validation functions on form submission
        $('form').submit(function (e) {
            var phoneNumberInput = $('#phone-number-input'); // Replace with the ID of your phone number input field
            var emailInput = $('#email-input'); // Replace with the ID of your email input field
    
            if (!validatePhoneNumber(phoneNumberInput)) {
                e.preventDefault();
            }
    
            if (!validateEmail(emailInput)) {
                e.preventDefault();
            }
        });
    
        // Restrict phone number input to numbers only
       
$('#phone-number-input').on('input', function () {
    var inputValue = $(this).val();
    var numericValue = inputValue.replace(/[^0-9]/g, '');
    $(this).val(numericValue);
});
    
    })(jQuery);
 